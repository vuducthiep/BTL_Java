    private void XacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XacNhanActionPerformed
        // TODO add your handling code here:
        String maNV = inputCode.getText();
        int check = 0;
        Iterator<NhanVien> iterator = parentFrame.list.iterator();
        while (iterator.hasNext()) {
            NhanVien nv = iterator.next();
            if (nv.getMa().equals(maNV)) {
                iterator.remove();
                check = 1;
                JOptionPane.showMessageDialog(this, "Xóa thành công nhân viên " + nv.getTen(), "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                parentFrame.refreshList();
                break; // Thoát khỏi vòng lặp khi đã xóa thành công
            }
        }
        if(check==1){
            this.dispose();
           parentFrame.setVisible(true);
        }
        
        if (check == 0) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên muốn xóa", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_XacNhanActionPerformed

