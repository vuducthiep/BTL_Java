    private void XacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_XacNhanActionPerformed
        // TODO add your handling code here:
        if(!validateInput()){
            return;
        }
        String maNV = inputCode.getText();
        if (maNV.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã nhân viên và tìm kiếm", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        try {
            int tuoi = Integer.parseInt(fixAge.getText());
            double luong = Double.parseDouble(fixSalary.getText());
            int soNgay = Integer.parseInt(fixDay.getText());
            boolean found = false;
            for (NhanVien nv : parentFrame.list) {
                if (nv.getMa().equals(maNV)) {
                    nv.setMa(fixCode.getText());
                    nv.setTen(fixName.getText());
                    nv.setTuoi(tuoi);
                    nv.setDiaChi(fixAddress.getText());
                    nv.setHeSoLuong(luong);
                    nv.setSoNgayCong(soNgay);
                    // Đánh dấu giới tính
                    if (gioiTinhNam.isSelected()) {
                        nv.setGioiTinh("Nam");
                    } else if (gioiTinhNu.isSelected()) {
                        nv.setGioiTinh("Nữ");
                    } else {
                        nv.setGioiTinh("Khác");
                    }
                    found = true;
                    break;
                }
            }

            if (found) {
                JOptionPane.showMessageDialog(this, "Sửa thông tin nhân viên thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                
                this.dispose();
                parentFrame.setVisible(true);
                parentFrame.refreshList();
                
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên với mã đã nhập", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tuổi và lương hợp lệ", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

        

       
        
    }//GEN-LAST:event_XacNhanActionPerformed

