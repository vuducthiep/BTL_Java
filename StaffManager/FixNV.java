    private boolean validateInput() {
    // Lấy thông tin từ các trường nhập liệu
    // Kiểm tra xem các trường nhập liệu có rỗng không
    if (fixCode.getText().isEmpty() || fixName.getText().isEmpty() || fixAge.getText().isEmpty() ||
        fixAddress.getText().isEmpty() || fixSalary.getText().isEmpty() || fixDay.getText().isEmpty() || 
        fixPosition.getText().isEmpty() || (!gioiTinhNam.isSelected() && !gioiTinhNu.isSelected() && !gioiTinhKhac.isSelected())) {
        JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    try {
        // Kiểm tra và chuyển đổi các trường số
        int tuoiNV = Integer.parseInt(fixAge.getText());
        if (tuoiNV <= 0) {
            JOptionPane.showMessageDialog(this, "Tuổi phải là một số nguyên dương.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        double luongNV = Double.parseDouble(fixSalary.getText());
        if (luongNV <= 0) {
            JOptionPane.showMessageDialog(this, "Lương phải là một số dương.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        int soNgayNV = Integer.parseInt(fixDay.getText());
        if (soNgayNV <= 0 || soNgayNV > 31) {
            JOptionPane.showMessageDialog(this, "Số ngày công phải là một số nguyên dương từ 1 đến 31.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return false;
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng cho tuổi, lương và số ngày công.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        return false;
    }

    return true;
}
    
