    private void timKiemButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timKiemButtonActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        String maNV = inputCode.getText();
        if (maNV.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã nhân viên và tìm kiếm", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        int check = 0;

        Iterator<NhanVien> iterator = parentFrame.list.iterator();
        while (iterator.hasNext()) {
            NhanVien nv = iterator.next();
            if (nv.getMa().equals(maNV)) {

                check = 1;
                // Hiển thị thông tin nhân viên vào các trường
                fixCode.setText(nv.getMa());
                fixName.setText(nv.getTen());
                fixAge.setText(String.valueOf(nv.getTuoi()));
                fixAddress.setText(nv.getDiaChi());
                fixSalary.setText(String.valueOf(nv.getHeSoLuong()));
                fixDay.setText(String.valueOf(nv.getSoNgayCong()));
                // Đánh dấu giới tính
                fixPosition.setText(nv.getChucVu());
                if (nv.getGioiTinh().equals("Nam")) {
                    gioiTinhNam.setSelected(true);
                } else if (nv.getGioiTinh().equals("Nữ")) {
                    gioiTinhNu.setSelected(true);
                } else {
                    gioiTinhKhac.setSelected(true);
                }
                break; // Thoát khỏi vòng lặp khi đã tìm thấy
            }
        }
        if (check == 0) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên muốn sửa, vui lòng nhập lại", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        }
        
    }//GEN-LAST:event_timKiemButtonActionPerformed

